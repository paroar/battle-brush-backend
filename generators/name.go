import (
	"fmt"
	"math/rand"
	"time"
)

// Name returns a random name
func Name() string {
	rand.Seed(time.Now().Unix())
	adj := adj[rand.Intn(len(adj))]
	name := names[rand.Intn(len(names))]
	fullName := fmt.Sprintf("%s_%s", adj, name)
	return fullName
}

